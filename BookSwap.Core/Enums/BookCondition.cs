namespace BookSwap.Core.Enums
{
    public enum BookCondition
    {
        New = 0,      // الكتاب جديد تمامًا، لم يُستخدم
        LikeNew = 1,  // الكتاب شبه جديد، بحالة ممتازة مع استخدام طفيف
        Good = 2,     // الكتاب بحالة جيدة، قد يحتوي على بعض علامات الاستخدام
        Fair = 3       // الكتاب بحالة مقبولة، يحتوي على عيوب واضحة ولكنه قابل للقراءة
    }
}
